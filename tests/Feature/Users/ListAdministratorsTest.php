<?php

namespace Tests\Feature\Users;

use App\Enums\ReviewStatusEnum;
use App\Models\Role;
use App\Models\User;
use Database\Seeders\RoleSeeder;
use Illuminate\Foundation\Testing\RefreshDatabase;
use Illuminate\Foundation\Testing\WithFaker;
use Tests\TestCase;

class ListAdministratorsTest extends TestCase
{
    use RefreshDatabase;
    private User $admin, $user, $reviewer;

    private String $url = '/api/v1/admin/admins';

    protected function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub
        $this->seed(RoleSeeder::class);
        $this->admin = User::factory()->create();
        $this->user = User::factory()->create();
        $this->reviewer = User::factory()->create();
        $this->reviewer->roles()->attach(Role::get(ReviewStatusEnum::REVIEWER));
        $this->admin->roles()->attach(Role::get(ReviewStatusEnum::ADMIN));
        $this->user->roles()->attach(Role::get(ReviewStatusEnum::USER));
    }

    // test that only admin gets all users
    public function test_to_get_all_users(): void
    {

        $response = $this->actingAs($this->admin)->get($this->url);

        // 200 status
        $response->assertStatus(200);
        // ensure only roles with users are listed
        $response->assertJsonCount(1, 'data');
        $response->assertJsonMissing(['email' => $this->user->email]);
        $response->assertJsonMissing(['email' => $this->reviewer->email]);
        // check that it returns user data
        $response->assertJsonFragment(['name' => $this->admin->name]);

    }

    public function test_only_admin_can_get_users(): void
    {

        $response = $this->actingAs($this->user)->get($this->url);

        // 200 status
        $response->assertStatus(403);
    }

    public function test_public_users_cannot_get_users(): void
    {
        $response = $this->getJson($this->url);

        $response->assertStatus(401);
    }
}
